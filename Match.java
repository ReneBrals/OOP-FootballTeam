import java.io.*;
import java.util.*;

public class Match {
	
      private Int Home;
      private Int Away;
      private String Opponent;
	  
	    public int getHome() {
	        return Home;
	    }
	  
	    public void setHome(int home) {
	        Home = home;
	    }
	    
	    public int getAway() {
	        return Away;
	    }
	  
	    public void setAway(int away) {
	        Away = away;
	    }
	    
	    public String getOpponent() {
	        return Opponent;
	    }
	  
	    public void setOpponent(String opponent) {
	        Opponent = opponent;
	    }
	  
}
